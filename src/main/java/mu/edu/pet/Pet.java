package mu.edu.pet;

public class Pet {
	private int id;
	private String name;
	private String type;
	private String species;
	private int age;
	private boolean adopted;
	
	public Pet(int id, String name, String type, String species, int age, boolean adopted) {
		super();
		this.id = id;
		this.name = name;
		this.type = type;
		this.species = species;
		this.age = age;
		this.adopted = adopted;
	}
	
}
